    private void fmtMaNVKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fmtMaNVKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String pcode = fmtMaNV.getText().trim();
            if (!pcode.isEmpty()) {
                try (Connection con = Connect.getConnection(); PreparedStatement pst = con.prepareStatement("SELECT * FROM nhanvien WHERE manv = ?")) {
                    pst.setString(1, pcode);
                    try (ResultSet rs = pst.executeQuery()) {
                        if (!rs.next()) {
                            new MyDialog("Không tồn tại nhân viên có mã này!!!!", MyDialog.ERROR_DIALOG);
                            fmtMaNV.setText("");
                        } else {
                            String ho = rs.getString("ho");
                            String ten = rs.getString("ten");
                            txtHoTen.setText(ho.trim() + " " + ten.trim());
                            fmtMaHD.setEnabled(true);
                            fmtMaKH.setEnabled(true);
                            dsNgayTao.setEnabled(true);
                            txtGhiChu.setEnabled(true);
                        }
                    }
                } catch (Exception ex) {
                    new MyDialog("Mã nhân viên không hợp lệ!!!!", MyDialog.ERROR_DIALOG);
                    fmtMaNV.setText("");
                    Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_fmtMaNVKeyPressed

