    private void fmtMaHDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fmtMaHDKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                String pcode = fmtMaHD.getText().trim();
                if (pcode.isEmpty() || !pcode.matches("\\d+")) {
                    new MyDialog("Mã hóa đơn không hợp lệ!", MyDialog.ERROR_DIALOG);
                    fmtMaHD.setText("");
                    return;
                }

                Connection con = Connect.getConnection();
                PreparedStatement pst = con.prepareStatement("SELECT * FROM hoadon WHERE MaHD = ?");
                pst.setString(1, pcode);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    new MyDialog("Mã hóa đơn đã tồn tại!", MyDialog.ERROR_DIALOG);
                    fmtMaHD.setText("");
                }

                rs.close();
                pst.close();
                con.close();
            } catch (Exception ex) {
                Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_fmtMaHDKeyPressed
