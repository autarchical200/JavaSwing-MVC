    private void insertHoaDonCSDL() {

        try {
            // Xây dựng câu lệnh SQL để insert dữ liệu
            Connection conn = Connect.getConnection();
            String sql = "INSERT INTO hoadon(MaHD, MaKH, MaNV, NgayLap, TongTien, GhiChu) VALUES (?, ?, ?, ?, ?, ?)";

            // Tạo PreparedStatement object để chứa câu lệnh SQL
            PreparedStatement pstmt = conn.prepareStatement(sql);

            // Thiết lập các tham số cho PreparedStatement object
            pstmt.setString(1, fmtMaHD.getText());
            pstmt.setString(2, fmtMaKH.getText());
            pstmt.setString(3, fmtMaNV.getText());
            pstmt.setDate(4, new java.sql.Date(dsNgayTao.getDate().getTime())); // Chuyển đổi từ java.util.Date sang java.sql.Date
            pstmt.setDouble(5, tongtatca);
            pstmt.setString(6, txtGhiChu.getText());

            // Thực thi câu lệnh SQL để insert dữ liệu
            pstmt.executeUpdate();

            // Đóng PreparedStatement object
            pstmt.close();

        } catch (Exception ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

