    private void xuLyThemVaoGioHang() throws Exception {

        dtmGioHang = (DefaultTableModel) tblGioHang.getModel();
        int row = tblSanPham.getSelectedRow();
        if (row < 0) {
            return;
        }
        String ma = fmfMaSP.getText();
        String ten = txtTenSP.getText();
        String donGia = fmfDonGia.getText();
        int soLuong = Integer.parseInt(fmfSoLuong.getText());
        int soLuongConLai = Integer.parseInt(tblSanPham.getValueAt(tblSanPham.getSelectedRow(), 4) + "");

        if (soLuong > soLuongConLai || soLuongConLai <= 0) {
            new MyDialog("Sản phẩm đã hết hàng", MyDialog.ERROR_DIALOG);
            return;
        }

        if (ma.trim().equalsIgnoreCase("")) {
            return;
        }
        int key = Integer.parseInt(ma);
        for (int i = 0; i < tblGioHang.getRowCount(); i++) {
            int maTbl = Integer.parseInt(tblGioHang.getValueAt(i, 0) + "");
            if (maTbl == key) {
                int soLuongAdd = Integer.parseInt(tblGioHang.getValueAt(i, 2) + "");
                soLuongAdd += soLuong;
                donGia = donGia.replace(",", "").replace(".", "");
                float donGiaSP = Float.parseFloat(donGia);

                tblGioHang.setValueAt(soLuongAdd, i, 2);
                tblGioHang.setValueAt(dcf.format(soLuong * donGiaSP), i, 4);

                // cập nhật lại số lượng trong db
                spBUS.capNhatSoLuongSP(key, -soLuong);
                spBUS.docListSanPham();
                getSanPham();
                return;
            }
        }
        Vector lst = new Vector();

        lst.add(ma);
        lst.add(ten);
        lst.add(soLuong);
        lst.add(donGia);
        donGia = donGia.replace(",", "");
        double donGiaSP = Double.parseDouble(donGia);
        double tong = (soLuong * donGiaSP);

        DecimalFormat df = new DecimalFormat("#,###");
        lst.add(df.format(tong));
//         cập nhật lại số lượng trong db
        spBUS.capNhatSoLuongSP(key, -soLuong);
        spBUS.docListSanPham();
        getSanPham();

        dtmGioHang.addRow(lst);
        new MyDialog("Thêm thành công!!", MyDialog.SUCCESS_DIALOG);

        // xu ly xuat hoa don
        itemName.add(ten);
        quantity.add(String.valueOf(soLuong));
        itemPrice.add(donGia);
        subtotal.add(String.valueOf(tong));
        tongtatca = (tongtatca + tong);

        //chuyen du lieu
//        DefaultTableModel newTableModel = new DefaultTableModel();
//        newTableModel.setDataVector(dtmGioHang.getDataVector(), dtmGioHang.getDataVector());
    }

    public PageFormat getPageFormat(PrinterJob pj) {

        PageFormat pf = pj.defaultPage();
        Paper paper = pf.getPaper();
        double bodyHeight = bHeight;
        double headerHeight = 5.0;
        double footerHeight = 10.0;
        double width = cm_to_pp(16);
        double height = cm_to_pp(headerHeight + bodyHeight + footerHeight);
        paper.setSize(width, height);
        paper.setImageableArea(0, 10, width, height - cm_to_pp(1));

        pf.setOrientation(PageFormat.PORTRAIT);
        pf.setPaper(paper);

        return pf;
    }

