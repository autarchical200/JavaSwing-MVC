    private ImageIcon getAnhSP(String src) {
        src = src.trim().equals("") ? "D:\\NetBeansProjects\\mvc2023\\qlpizza\\src\\main\\resources\\Images\\SanPham\\default.png" : src;
        //Xử lý ảnh
        BufferedImage img = null;
        File image = new File(src);

        if (!image.exists()) {
            src = "default.png";
            image = new File("D:\\NetBeansProjects\\mvc2023\\qlpizza\\src\\main\\resources\\Images\\SanPham" + src);
        }

        try {
            img = ImageIO.read(image);
            fileAnhSP = new File(src);
        } catch (IOException e) {
            fileAnhSP = new File("D:\\NetBeansProjects\\mvc2023\\qlpizza\\src\\main\\resources\\Images\\SanPham");
        }

        if (img != null) {
            Image dimg = img.getScaledInstance(200, 200, Image.SCALE_SMOOTH);
            return new ImageIcon(dimg);
        }

        return null;
    }

