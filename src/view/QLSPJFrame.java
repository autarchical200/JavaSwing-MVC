    private void xuLySuaSanPham() throws Exception {
    try {
        String anh = fileAnhSP.getName();
        String masp = fmfMaSP.getText();
        int maLoai = Integer.parseInt(cbMaLoai.getSelectedItem().toString());
        int soLuongSP = Integer.parseInt(fmfSoLuong.getText());
        String tenSP = txtTenSP.getText();
        String dVT = txtDVT.getText();
        float donGiaSP = Float.parseFloat(fmfDonGia.getText().replace(",", ""));
        if (masp.equals("")) {
            new MyDialog("Chưa chọn sản phẩm để xóa!!", MyDialog.ERROR_DIALOG);
        } else {
            SanPhamModel sp = new SanPhamModel();

            int maSP = Integer.parseInt(masp);
            sp.setMasp(maSP);
            sp.setTensp(tenSP);
            sp.setMaloai(maLoai);
            sp.setSoluong(soLuongSP);
            sp.setDonvitinh(dVT);
            sp.setHinhanh(anh);
            sp.setDongia(donGiaSP);
            SanPhamDAO spdao = new SanPhamDAO();
            if (spdao.suaSanPham(sp)) {
                new MyDialog("Sửa thành công!", MyDialog.SUCCESS_DIALOG);
                LoadSPTable();
                luuFileAnh();
            } else {
                new MyDialog("Sửa thất bại!", MyDialog.ERROR_DIALOG);
            }
        }
    } catch (NumberFormatException ex) {
        new MyDialog("Chưa chọn sản phẩm để sửa!", MyDialog.ERROR_DIALOG);
    }
}


